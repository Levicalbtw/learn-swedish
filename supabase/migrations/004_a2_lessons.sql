'# En Katt, Två Katter — Plural Nouns

## The 5 Plural Endings
Unlike English, which mostly just adds "-s", Swedish has 5 different ways to make a noun plural. The ending depends on whether the word is an **en-word** or an **ett-word**, and what its final vowel is.

Here are the 5 endings to remember: **-or**, **-ar**, **-er**, **-n**, and **nothing**!

### 1. The "-or" Group (En-words ending in -a)
If an en-word ends in "a", drop the "a" and add **-or**.
- en flicka → **flickor** (girls)
- en klocka → **klockor** (clocks)

### 2. The "-ar" Group (Short En-words)
Short en-words that end in a consonant often take **-ar**.
- en bil → **bilar** (cars)
- en hund → **hundar** (dogs)

### 3. The "-er" Group (International En-words)
En-words ending in consonants (often loanwords) or ending in "-nad", "-het", "-skap" take **-er**.
- en katt → **katter** (cats)
- en student → **studenter** (students)

### 4. The "-n" Group (Ett-words ending in a vowel)
If an ett-word ends in a vowel, just add an **-n**.
- ett äpple → **äpplen** (apples)
- ett kvitto → **kvitton** (receipts)

### 5. The "No Change" Group (Ett-words ending in a consonant)
If an ett-word ends in a consonant, the plural is exactly the same as the singular!
- ett hus → **hus** (houses)
- ett barn → **barn** (children)

> 💡 **Trick:** If someone points at two kids and you get confused, just say "två barn". It stays the same!

## Practice
Make these plural:
1. en bil → **bilar**
2. ett hus → **hus**
3. en flicka → **flickor**'),

