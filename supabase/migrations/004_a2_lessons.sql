'# Framtid — The Future Tense

## The Two Ways to Say "Will"
In Swedish, there is no single future tense. Instead, we use helper words combined with the base form of the verb (the infinitive). The two main ways are **"ska"** and **"kommer att"**.

## 1. Ska (Intent or Plan)
Use **ska** when someone is *planning* or *deciding* to do something. It implies a conscious choice. English equivalent: "I am going to..."

> **Jag ska resa till Sverige.** — I am going to travel to Sweden.
> **Vi ska äta middag nu.** — We are going to eat dinner now.

*(Notice we use "resa" and "äta" in their base forms, not "reser" or "äter"!)*

## 2. Kommer att (Prediction or Fact)
Use **kommer att** when you are predicting something or when something is out of your control. English equivalent: "It will happen."

> **Det kommer att regna i morgon.** — It will rain tomorrow.
> **Du kommer att gilla filmen.** — You will like the movie.

## 💡 The Shortcut: Just using Present Tense!
Swedes are highly efficient. Very often, they don''t even bother with future tense words! If you add a future time word (like "imorgon" - tomorrow), you can just use the **present tense**!

> **Jag jobbar imorgon.** — I work tomorrow. (Instead of "I am going to work tomorrow")
> **Tåget går klockan fem.** — The train leaves at five.

## Practice
Should you use *ska* or *kommer att*?
1. It is going to snow. → Det **kommer att** snöa.
2. I am going to drink coffee. → Jag **ska** dricka kaffe.')

