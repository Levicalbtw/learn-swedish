'# Dåtid — What did you do yesterday?

## How to Talk About the Past
To talk about completed actions in the past (like "I ate" or "We watched"), Swedish uses the **Preteritum** tense.

Just like the present tense, the verb **does not change based on who is doing it**. "I ran" and "They ran" use the exact same verb form!

## Group 1: Verbs ending in -a (Add -de)
If a verb ends in "-a", the past tense usually takes **-de**.
- prata (to talk) → **pratade** (talked)
- jobba (to work) → **jobbade** (worked)

> **Jag jobbade igår.** — I worked yesterday.

## Group 2: Verbs ending in consonants (Add -te or -de)
- läsa (to read) → **läste** (read)
- ringa (to call) → **ringde** (called)

> **Hon läste en bok.** — She read a book.

## Group 3: Strong Verbs (Vowel Change)
The most common verbs in Swedish are irregular. They don''t take an ending; instead, the vowel changes! You just have to memorize these.

| Present | Past | English |
|---------|------|---------|
| är (am/is/are) | **var** | was/were |
| har (have) | **hade** | had |
| går (go) | **gick** | went |
| gör (do) | **gjorde** | did |
| äter (eat) | **åt** | ate |

## 💡 Watch the V2 Rule!
If you start a sentence with "Igår" (Yesterday), remember that the verb must still be in the second position!

✅ **Igår åt jag pizza.**
❌ ~~Igår jag åt pizza.~~

## Practice
Translate to the past tense:
1. Jag _____ en hund. (had) → **hade**
2. Han _____ hem. (went) → **gick**
3. Vi _____ pizza. (ate) → **åt**'),

