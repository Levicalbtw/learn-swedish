Textbook Swedish is great, but to understand people on the street or in casual settings, you need to understand *talspråk* (spoken language) and slang. Swedes shorten many words and use a lot of colorful filler words.

## Common Spoken Abbreviations

When Swedes speak quickly, they drop letters:
* **"Någon" / "Något" / "Några"** (some/any) becomes -> **Nån / Nåt / Nåra**
* **"Sådan" / "Sådant" / "Sådana"** (such) becomes -> **Sån / Sånt / Såna**
* **"Sedan"** (then/since) becomes -> **Sen**
* **"Att"** (to/that) is almost always pronounced like **"Å"** (o) before verbs. (e.g., *Jag gillar att cykla* sounds like *Jag gillar å cykla*)

