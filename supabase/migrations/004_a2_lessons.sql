'# Adjektiv — Matching the Noun

## The 3 Forms of an Adjective
In Swedish, adjectives have to agree with the noun they describe. This means an adjective changes its spelling depending on whether the noun is an **en-word**, an **ett-word**, or **plural**.

Let''s look at the adjective **röd** (red):

| Noun Type | Ending | Example | English |
|-----------|--------|---------|---------|
| **en-word** | - (none) | en **röd** bil | a red car |
| **ett-word** | -t | ett **rött** hus | a red house |
| **Plural** | -a | två **röda** bilar | two red cars |

## 💡 The "A" Rule for Plural
If you are talking about multiple things, the adjective almost always ends in **-a**.
- en stor hund (a big dog) → två **stora** hundar (two big dogs)
- ett litet barn (a small child) → två **små** barn (two small children)

## Liten & Stor (Small & Big)
These are two of the most popular adjectives, but "liten" is highly irregular!

**Stor (Big)**
- en **stor** hund
- ett **stort** hus
- två **stora** hundar

**Liten (Small)**
- en **liten** hund
- ett **litet** hus
- två **små** hundar *(completely changes!)*

## Practice
